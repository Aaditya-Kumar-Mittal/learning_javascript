let s = "My name is Aaditya Mittal.";

console.log(s[0]); //prints 1st character of the string

console.log(s.indexOf("is")); //returns first index of the given character or string

console.log(s.lastIndexOf("a")); //returns last occurrence of the given string or character

console.log(s.charAt(5)); //returns the character at the given index

console.log(s.endsWith("Mittal")); //returns a boolean value, if the given string ends with a particular string or not

console.log(s.includes(" Aaditya ")); //whether the given string includes a particular string or not, returns a boolean value
